    Spec.newVersion = function () {
        if (!Swarm.root.author) throw new Error('Swarm.author not set');
        if (Spec.frozen)
            return Spec.frozen;
        var ts = Spec.date2ts(new Date()), seq='';
        if (ts===Spec.lastTs)
            seq = Spec.int2base(++Spec.seq,2); // max ~4000Hz
        else
            Spec.seq = 0;
        Spec.lastTs = ts;
        return ts + seq + '+' + Swarm.root.author;
    };
